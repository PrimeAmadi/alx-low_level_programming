#include <stdio.h>
/**
 * op_add - adds
 * @a: a to
 * @b: b
 * Return: answer
 */
int op_add(int a, int b)
{
	return (a + b);
}



/**
 * op_sub - subtracts
 * @a: a to
 * @b: b
 * Return: answer
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies
 * @a: a to
 * @b: b
 * Return: answer
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides
 * @a: a by
 * @b: b
 * Return: answer
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - find mod
 * @a: a to
 * @b: b
 * Return: answer
 */
int op_mod(int a, int b)
{
	return (a % b);
}

